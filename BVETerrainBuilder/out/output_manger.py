from shapely import box
import geopandas as gpd

class OutputExporter:
    @staticmethod
    def save_shapefile(segments):
        """구간별 SHP로 저장"""
        for idx, (minx, miny, maxx, maxy) in enumerate(segments, start=1):

            poly = box(minx, miny, maxx, maxy)
            gdf = gpd.GeoDataFrame([{"id": idx, "geometry": poly}], crs="EPSG:5186")
            out_shp = f"C:/temp/shp/segment_{idx}.shp"
            gdf.to_file(out_shp)

    @staticmethod
    def save_qml(segments):
        #템플릿
        qml_template = """<!DOCTYPE qgis PUBLIC 'http://mrcc.com/qgis.dtd' 'SYSTEM'>
            <qgis version="3.16" styleCategories="Symbology">
              <renderer-v2 type="singleSymbol">
                <symbols>
                  <symbol name="0" type="fill" alpha="1">
                    <layer pass="0" class="SimpleFill" locked="0">
                      <prop k="color" v="255,255,255,0"/> <!-- 채우기 없음 -->
                      <prop k="outline_color" v="0,0,0,255"/> <!-- 외곽선 검정 -->
                      <prop k="outline_width" v="0.1"/> <!-- 외곽선 두께 -->
                      <prop k="style" v="no"/> <!-- 내부 채움 없음 -->
                    </layer>
                  </symbol>
                </symbols>
              </renderer-v2>
            </qgis>
            """
        # 구간별 QML 생성
        for idx in range(1, len(segments) + 1):
            qml_path = f"C:/temp/shp/segment_{idx}.qml"
            with open(qml_path, "w", encoding="utf-8") as f:
                f.write(qml_template)
            # print("QML 저장 완료:", qml_path)

    @staticmethod
    def save_obj_with_groups(filepath, save_items):
        """
        save_items: [(vertices, faces, color, name), ...]
        """
        cumulative_offset = 0

        with open(filepath, 'w', encoding='utf-8') as f:
            f.write("# Generated by Gemini Rail System\n")

            for vertices, faces, color, name in save_items:
                # 1. 그룹 이름 작성
                f.write(f"g {name}\n")

                # 2. 정점(Vertices) 작성
                for v in vertices:
                    f.write(f"v {v[0]:.6f} {v[1]:.6f} {v[2]:.6f}\n")

                # 3. 면(Faces) 작성
                for face in faces:
                    # OBJ 인덱스는 1부터 시작하며, 이전 메쉬들의 정점 개수(cumulative_offset)를 더해줌
                    # f 1 2 3 -> f (1+offset) (2+offset) (3+offset)
                    f_indices = [str(idx + 1 + cumulative_offset) for idx in face]
                    f.write(f"f {' '.join(f_indices)}\n")

                # 4. 다음 메쉬를 위해 오프셋 업데이트
                cumulative_offset += len(vertices)
                f.write("\n")
